     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        
 
        $city = City::find($id);
        if ((!$city) || ($city->visible == 0)) {
            abort(404);
        }
        $citizen = Citizen::where('city_id', $city->id)->orderBy('f_name')->get();
        
        $data = [
            'city' => $city,
            'citizen' => $citizen,
        ];

        return view('city.show')->with($data);
    }

